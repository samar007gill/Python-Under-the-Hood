# ❌ Incorrect Example: Python will interpret 'ifx' as a single variable name, 
# instead of recognizing 'if' as a keyword.
ifx = 10  # This is treated as a variable, not a conditional statement.

# ✅ Correct Example: Proper use of whitespace to separate the 'if' keyword from the variable 'x'.
if x == 10:  # Here, 'if' is correctly recognized as a keyword.
    print("x is equal to 10")  # Indented block inside the 'if' statement.

