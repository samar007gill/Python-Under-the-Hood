# ⚡ Mastering Python Operators: The Building Blocks of Expressions 🧮🔗

# 1️⃣ Arithmetic Operators ➕➖✖️➗
a = 10
b = 3

print("🔹 Arithmetic Operators:")
print("Addition:", a + b)      
print("Subtraction:", a - b)   
print("Multiplication:", a * b)  
print("Division:", a / b)      
print("Modulus:", a % b)       
print("Exponentiation:", a ** b) 
print("Floor Division:", a // b) 
print("\n")

# 2️⃣ Comparison Operators 🔍⚖️
print("🔹 Comparison Operators:")
print("a < b:", a < b)   
print("a <= b:", a <= b)  
print("a > b:", a > b)   
print("a >= b:", a >= b)  
print("a == b:", a == b)  
print("a != b:", a != b)  
print("\n")

# 3️⃣ Logical Operators 🧠💡
x = True
y = False

print("🔹 Logical Operators:")
print("x and y:", x and y)  
print("x or y:", x or y)    
print("not x:", not x)      
print("\n")

# 4️⃣ Bitwise Operators 🧮🖥️
a = 5  # 101 in binary
b = 3  # 011 in binary

print("🔹 Bitwise Operators:")
print("Bitwise AND:", a & b)  
print("Bitwise OR:", a | b)   
print("Bitwise XOR:", a ^ b)  
print("Bitwise NOT (~a):", ~a) 
print("Left Shift (a << 1):", a << 1) 
print("Right Shift (a >> 1):", a >> 1) 
print("\n")

# 5️⃣ Assignment Operators 🖊️📝
x = 5
print("🔹 Assignment Operators:")
x += 3  
print("x after += 3:", x)  

x *= 2  
print("x after *= 2:", x)  
print("\n")

# 6️⃣ Identity & Membership Operators 🔍👁️
a = [1, 2, 3]
b = a  
c = [1, 2, 3]  

print("🔹 Identity Operators:")
print("a is b:", a is b)  
print("a is c:", a is c)  
print("\n")

print("🔹 Membership Operators:")
print("2 in a:", 2 in a)  
print("5 not in a:", 5 not in a)  
